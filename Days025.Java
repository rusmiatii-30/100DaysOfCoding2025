
package Minggu2;
public class Days025 {
    public static void main(String[] args) {
        
        int D = 30;
        D++;
        D--;
        
        System.out.println("Angka : " + D++);
        System.out.println("Angka : " + D--);
        
        System.out.println("Angka : " + ++D);
        System.out.println("Angka : " + --D);
    }
    
}
